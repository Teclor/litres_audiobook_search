use audiobook;
CREATE TABLE IF NOT EXISTS book_data (
    BOOK_ID INT NOT NULL,
    IS_AVAILABLE BOOL,
    URL VARCHAR(255),
    PRICE DECIMAL(10, 2),
    CURRENCY_ID CHAR(3),
    PICTURE VARCHAR(255),
    AUTHOR VARCHAR(100),
    PUBLISHER VARCHAR(100),
    SERIES VARCHAR(100),
    YEAR YEAR,
    ISBN CHAR(17),
    PERFORMED_BY VARCHAR(100),
    FORMAT VARCHAR(10),
    DESCRIPTION TEXT,
    IS_DOWNLOADABLE BOOL,
    AGE SMALLINT,
    LANG CHAR(2),
    PARAM_FORMATS VARCHAR(255),
    PARAM_FRAGMENT VARCHAR(255),
    LITRES_ISBN CHAR(17),
    GENRES_LIST VARCHAR(255),
    FOREIGN KEY (BOOK_ID) REFERENCES book_name (BOOK_ID),
    PRIMARY KEY (BOOK_ID)
);