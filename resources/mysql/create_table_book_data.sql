use audiobook;
CREATE TABLE IF NOT EXISTS book_data (
    BOOK_ID INT NOT NULL,
    IS_AVAILABLE BOOL,
    URL VARCHAR(255),
    PRICE DECIMAL(10, 2),
    CURRENCY_ID CHAR(3),
    CATEGORY_ID INT,
    PICTURE VARCHAR(255),
    AUTHOR VARCHAR(255),
    PUBLISHER VARCHAR(255),
    SERIES VARCHAR(500),
    YEAR VARCHAR(100),
    ISBN VARCHAR(255),
    PERFORMED_BY VARCHAR(255),
    FORMAT VARCHAR(10),
    DESCRIPTION TEXT,
    IS_DOWNLOADABLE BOOL,
    AGE SMALLINT,
    LANG CHAR(2),
    PARAM_FORMATS VARCHAR(255),
    PARAM_FRAGMENT VARCHAR(255),
    LITRES_ISBN VARCHAR(255),
    GENRES_LIST VARCHAR(500),
    FOREIGN KEY (BOOK_ID) REFERENCES book_name (BOOK_ID) ON DELETE CASCADE,
    PRIMARY KEY (BOOK_ID)
);